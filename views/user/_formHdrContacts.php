You may search for and select a contact.
Type in several letters to display a list of all user names containing these letters.
If necessary, backspace and change the letters to display users similar to your search criteria.<br>

If your list contains the desired contact
<ul>
    <li>click on it.</li>
    <li>correct the optional mobile and email fields if necessary. Do not correct any name spelling.
    </li>
    <li>click Next.</li>
</ul>
If you can not find the contact
<ul>
    <li>then type in the contact's first and last names
    <li>mobile and email are required for a new contact.</li>
    <li>click Next.</li>
</ul>
