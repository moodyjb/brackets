You may search for and select a player.
Type in several letters to display a list of all player names containing these letters.
If necessary, backspace and change the letters to display player similar to your search criteria.<br>

If your list contains the desired player
<ul>
    <li>click on it.</li>
    <li>correct the required birthdate and address fields if necessary. Do not correct any name spelling.</li>
    <li>mobile and email are optional</li>
    <li>click Next.</li>
</ul>
If you can not find the player
<ul>
    <li>then type in the player's first and last names
    <li>birthdate and address are required for a new player.</li>
    <li>DO NOT enter a player.</li>
    <li>click Next.</li>
</ul>
